                        required
                        autocomplete="new-password"
                    />
                    <button
                        type="button"
                        @click="togglePassword"
                        class="absolute inset-y-0 right-0 pr-3 flex items-center text-gray-400"
                    >
                        <svg
                            v-if="!showPassword"
                            xmlns="http://www.w3.org/2000/svg"
